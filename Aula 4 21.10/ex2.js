/* ==== FUNÇÕES PARA VETORES ==== */

let alunos = ["Cristiano", "Lobato", "Rafael", "Marcus"];
console.log(alunos);

console.log("\n== POP ===\n");

alunos.pop(); // remove o último elemento de um vetor;
console.log(alunos);

console.log("\n=== SHIFT ===\n");

alunos.shift(); //remove o primeiro elemento de um vetor;
console.log(alunos);

console.log("\n=== PUSH ===\n");

alunos.push("Yago"); // adicona algo no final de um vetor;
console.log(alunos);

console.log("\n=== UNSHIFT ===\n");
alunos.unshift("Luiz"); //adiciona algo no inicio do vetor;
console.log(alunos);

